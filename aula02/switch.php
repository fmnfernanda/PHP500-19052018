<?php

//switch

$op = readline('Digiete uma opcao: ');

switch($op){
    case 1:
        echo 'Voce digitou a opcao 1' .PHP_EOL;
        break;

    case 2:
        echo 'Voce digitou a opcao 2' .PHP_EOL;
        break;


    case 3:
        echo 'Voce digitou a opcao 3' .PHP_EOL;
        break;

    case 4:
        echo 'Voce digitou a opcao 4' .PHP_EOL;
        break;    

    default:
        echo 'Opcao invalida' .PHP_EOL;
        break;

}